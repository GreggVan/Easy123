package org.ez.data;

/**
 * Represents a JSON version of a Picassa photo.
 * @author jmouka
 *
 */
public class Photo {
	private String url;
	
	public Photo(String url) {
		super();
		this.url = url;
	}

	public String getUrl() {
		return url;
	}

	public void setUrl(String url) {
		this.url = url;
	}
	public String toString() {
		return url;
	}
	
}
